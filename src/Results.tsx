import { FunctionComponent } from "react";
import Pet from "./Pet";
import { Pet as PetType } from "./APIResponsesTypes";

interface Props {
  pets: PetType[];
}

const Results: FunctionComponent<Props> = ({ pets }) => {
  return (
    <div className="search">
      {!pets.length ? (
        <h1>No Pets Found</h1>
      ) : (
        pets.map((pet) => (
          <Pet
            animal={pet.animal}
            key={pet.id}
            name={pet.name}
            breed={pet.breed}
            images={pet.images}
            location={`${pet.city}, ${pet.state}`}
            id={pet.id}
          />
        ))
      )}
    </div>
  );
};

export default Results;
